// ================================================================
// 模組名稱：encoder_8_to_3
// 功能說明：
//   1) 將 8 位元 one-hot 輸入訊號編碼為 3 位元二進位輸出。 one-hot 意味著在任何時刻，輸入匯流排中只有一個位元為 1，其餘位元為 0。
//   2) 預期任一時刻僅有一個輸入位元為 1（有效）。
//   3) 若輸入不是合法 one-hot（例如全 0 或多位元同時為 1），
//      會進入 default 分支，輸出 3'b000。
//
// 對應關係：
//   in[0] -> out = 000
//   in[1] -> out = 001
//   in[2] -> out = 010
//   in[3] -> out = 011
//   in[4] -> out = 100
//   in[5] -> out = 101
//   in[6] -> out = 110
//   in[7] -> out = 111
// ================================================================
module encoder_8_to_3 (  	// 8 位元輸入匯流排（one-hot）：設定合法情況下僅其中一個位元為 1。
	input  wire [7:0] in,  
	output reg  [2:0] out  // 3 位元輸出：對應於輸入位元位置的二進位編碼。
);

// 組合邏輯區塊：
// 只要 in 任一位元改變，out 立即重新計算。
// always @(*) 表示這是一個組合邏輯區塊，輸出 out 的值完全由輸入 in 決定，且不依賴於時鐘訊號。
always @(*) begin
	// 依照 one-hot 輸入進行編碼。
	case (in)
		8'b0000_0001: out = 3'b000; // in[0] 有效
		8'b0000_0010: out = 3'b001; // in[1] 有效
		8'b0000_0100: out = 3'b010; // in[2] 有效
		8'b0000_1000: out = 3'b011; // in[3] 有效
		8'b0001_0000: out = 3'b100; // in[4] 有效
		8'b0010_0000: out = 3'b101; // in[5] 有效
		8'b0100_0000: out = 3'b110; // in[6] 有效
		8'b1000_0000: out = 3'b111; // in[7] 有效
		// 非法輸入保護：
		// - 全 0（無有效輸入）
		// - 多位元同時為 1（非 one-hot）
		default:      out = 3'b000;
	endcase
end

endmodule
